public class Perbandingan {
    public static void main(String[] args) {
        int c = 15;
        int b = 20;
        boolean hasil;

        hasil = c == b ;
        System.out.println("Apakah c == b ? " + hasil );

        hasil = c != b;
        System.out.println(" Apakah c != b ? " + hasil );

        hasil = c > b;
        System.out.println("Apakah c > b ? " + hasil );

        hasil = c < b;
        System.out.println(" Apakah c < b ? " + hasil );

        hasil = c >= b;
        System.out.println("Apakah c >= b ? " + hasil );

        hasil = c <= b;
        System.out.println("Apakah c <= b ? " + hasil);
    }
}

public class Tabung {
    public static void main(String[] args) {
        int r, t;
        double phi, luas;
        phi = 3.14;
        r = 6;
        t = 10;
        luas = 2 * phi * r * r + t;
        System.out.println("jari-jari-lingkaran =" + r);
        System.out.println("tinggi-tabung =" + t);
        System.out.println("luas-tabung =" + luas);
    }
    
}
